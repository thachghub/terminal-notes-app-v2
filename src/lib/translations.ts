// Helper function to get translated text with interpolation support
export function t(key: TranslationKey, language: Language = 'en', variables?: Record<string, string>): string {
  const languageTranslations = translations[language] || translations.en;
  const rawText = languageTranslations[key] || translations.en[key];
  let text = String(rawText);
  
  // Simple interpolation for variables like {{content}}, {{action}}
  if (variables) {
    Object.entries(variables).forEach(([varKey, varValue]) => {
      text = text.replace(new RegExp(`{{${varKey}}}`, 'g'), varValue);
    });
  }
  
  return text;
}

